-- 197_repair_service_aliases.sql
--
-- Synonym index for repair service names. Customers describe problems in
-- plain language ("lcd", "won't charge", "ghost touch") while the catalog
-- uses formal names ("Screen Replacement", "Charging Port Repair"). The
-- POS Issue-step filter joins on this table so a search for "lcd" still
-- surfaces Screen Replacement.
--
-- Synonyms are device-agnostic by default — "cracked screen" means screen
-- replacement whether the device is a phone, tablet, or laptop. The seed
-- below applies each alias to EVERY service that shares the canonical
-- name, so editing a single row in the settings UI updates all categories
-- at once. Shops that want per-category aliases can simply delete the
-- broad row and add per-(service_id) replacements.
--
-- Migration relies on `repair_services` having seeded entries with the
-- canonical names below (migrations 137 + 172 cover phone / laptop /
-- console / tablet / watch / xr). Aliases for any service that doesn't
-- exist yet are silently skipped by the JOIN — re-running this migration
-- after a later seed would re-insert them (guard via the UNIQUE index).

CREATE TABLE IF NOT EXISTS repair_service_aliases (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  repair_service_id INTEGER NOT NULL REFERENCES repair_services(id) ON DELETE CASCADE,
  alias TEXT NOT NULL,
  created_at TEXT NOT NULL DEFAULT (datetime('now')),
  updated_at TEXT NOT NULL DEFAULT (datetime('now')),
  UNIQUE (repair_service_id, alias)
);

CREATE INDEX IF NOT EXISTS idx_repair_service_aliases_service
  ON repair_service_aliases (repair_service_id);
CREATE INDEX IF NOT EXISTS idx_repair_service_aliases_alias_lower
  ON repair_service_aliases (LOWER(alias));

